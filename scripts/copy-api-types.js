/**
 * Copy API and Database Types Script
 *
 * Copies the shared type definitions from the @txg/shared workspace package
 * (packages/shared — the single source of truth, consumed directly by the
 * Angular app) into the Supabase Edge Functions directory.
 *
 * This transitional copy exists only because the Deno-based Edge Function
 * cannot import files outside supabase/functions/. It disappears together
 * with the Edge Functions once the backend moves to the Azure Function App
 * (which imports @txg/shared like any other workspace package).
 */

const fs = require("fs");
const path = require("path");

// Paths
const API_SOURCE_FILE = path.join(__dirname, "../packages/shared/src/api.types.ts");
const DB_SOURCE_FILE = path.join(__dirname, "../packages/shared/src/database.types.ts");
const API_TARGET_FILE = path.join(__dirname, "../supabase/functions/api/models/api.types.ts");
const DB_TARGET_FILE = path.join(__dirname, "../supabase/functions/api/models/database.types.ts");

// Function to copy and transform a file
function copyTypeFile(sourceFile, targetFile, type, transformContent = null) {
  console.log(`Reading from: ${sourceFile}`);
  let sourceContent = fs.readFileSync(sourceFile, "utf8");

  // Apply any transformations if provided
  if (transformContent && typeof transformContent === 'function') {
    sourceContent = transformContent(sourceContent);
  }

  // Add comment at the top
  const targetContent = `/**
 * !!! IMPORTANT: AUTO-GENERATED FILE !!!
 *
 * This file is automatically generated from packages/shared/src/${path.basename(sourceFile)}
 * Do not edit this file directly. Make changes to the @txg/shared package instead;
 * the pre-commit hook re-runs this script when packages/shared/src changes.
 */

${sourceContent}`;

  // Write to target file
  console.log(`Writing to: ${targetFile}`);
  fs.writeFileSync(targetFile, targetContent);
  console.log(`${type} types successfully copied!`);
}

// Copy Database types
copyTypeFile(DB_SOURCE_FILE, DB_TARGET_FILE, "Database");

// Copy API types with import path transformation (Deno requires explicit .ts extensions)
copyTypeFile(API_SOURCE_FILE, API_TARGET_FILE, "API", (content) => {
  return content.replace(
    `import type { Database } from './database.types';`,
    `import type { Database } from './database.types.ts';`,
  );
});

console.log("All types successfully synchronized!");
